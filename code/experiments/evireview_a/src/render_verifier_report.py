from __future__ import annotations

import json

from common import DATA_DIR, REPORT_DIR, ensure_dirs


def main() -> None:
    ensure_dirs()
    silver = json.loads((DATA_DIR / "weakness_evidence_silver_summary.json").read_text(encoding="utf-8"))
    verifier = json.loads((DATA_DIR / "verifier_rule_based_summary.json").read_text(encoding="utf-8"))

    lines = [
        "# Evidence Verifier Baseline Report",
        "",
        "This report summarizes the first rule-based verifier pass. It is a pipeline/debugging baseline, not a final evaluation against human gold labels.",
        "",
        "## Silver Label Bootstrap",
        "",
        f"- Source: `{silver['source']}`",
        f"- Silver items: {silver['silver_count']}",
        f"- Label counts: `{silver['label_counts']}`",
        "",
        "## Rule-based Verifier",
        "",
        f"- Verifier: `{verifier['verifier']}`",
        f"- Predictions: {verifier['prediction_count']}",
        f"- Prediction label counts: `{verifier['label_counts']}`",
        "",
        "## Interpretation",
        "",
        "- The current labels are generated by lexical coverage and category-section alignment rules.",
        "- They are useful for checking data flow and report structure before manual annotation.",
        "- They must not be reported as final accuracy, Macro-F1, or Evidence Recall@K.",
        "- The next valid evaluation step is to manually label `annotation_sheet_section_hybrid.csv`.",
    ]
    out_path = REPORT_DIR / "verifier_rule_based_report.md"
    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()

